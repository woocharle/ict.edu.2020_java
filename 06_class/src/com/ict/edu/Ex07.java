package com.ict.edu;

public class Ex07 {
	private String name = "커피우유";
	private int su = 100; 
	private int price = 1100;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSu() {
		return su;
	}
	
	
	public void setSu(int su) {
		this.su = su;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}

}
